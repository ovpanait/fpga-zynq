static void ciphertext_to_state(state_t state, uint8_t *ciphertext)
{
	int row, col;
	int ciphertext_i;

	for (col = 0, ciphertext_i = 0; col < Nb; ++col) {
		for (row = 0; row < Nb; ++row, ++ciphertext_i) {
			state[row][col] = ciphertext[ciphertext_i];
		}
	}

	#ifdef DEBUG
	dbg_state(__func__, state);
	#endif
}

static void state_to_plaintext(state_t state, uint8_t *plaintext)
{
	int row, col;
	int plaintext_i;

	for (col = 0, plaintext_i = 0; col < Nb; ++col) {
		for (row = 0; row < Nb; ++row, ++plaintext_i) {
			plaintext[plaintext_i] = state[row][col];
		}
	}

	#ifdef DEBUG
	dbg_state(__func__, state);
	#endif
}

static void inv_sub_bytes(state_t state)
{
	int row, col;

	for (row = 0; row < Nb; ++row) {
		for (col = 0; col < Nb; ++col) {
			state[row][col] = inv_get_sbox(state[row][col]);
		}
	}

	#ifdef DEBUG
	dbg_state(__func__, state);
	#endif
}

