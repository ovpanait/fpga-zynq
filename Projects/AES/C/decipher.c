static void inv_mix_columns(state_t state)
{
        int i;
        uint8_t a, b, c, d;

        for (i = 0; i < Nb; ++i)
        { 
          a = state[0][i];
          b = state[1][i];
          c = state[2][i];
          d = state[3][i];

          state[0][i] = multiply(a, 0x0e) ^ multiply(b, 0x0b) ^ multiply(c, 0x0d) ^ multiply(d, 0x09);
          state[1][i] = multiply(a, 0x09) ^ multiply(b, 0x0e) ^ multiply(c, 0x0b) ^ multiply(d, 0x0d);
          state[2][i] = multiply(a, 0x0d) ^ multiply(b, 0x09) ^ multiply(c, 0x0e) ^ multiply(d, 0x0b);
          state[3][i] = multiply(a, 0x0b) ^ multiply(b, 0x0d) ^ multiply(c, 0x09) ^ multiply(d, 0x0e);
        }

	#ifdef DEBUG
	dbg_state(__func__, state);
	#endif
}

static void inv_shift_rows(state_t state)
{
        uint8_t temp;

        temp = state[1][3];
        state[1][3] = state[1][2];
        state[1][2] = state[1][1];
        state[1][1] = state[1][0];
        state[1][0] = temp;

        temp = state[2][0];
        state[2][0] = state[2][2];
        state[2][2] = temp;

        temp = state[2][1];
        state[2][1] = state[2][3];
        state[2][3] = temp;

        temp = state[3][0];
        state[3][0] = state[3][1];
        state[3][1] = state[3][2];
        state[3][2] = state[3][3];
        state[3][3] = temp;

	#ifdef DEBUG
	dbg_state(__func__, state);
	#endif
}

void decipher(uint8_t *ciphertext, uint8_t *plaintext, uint8_t *sched)
{
        uint8_t round = Nr;
        state_t state;
        ciphertext_to_state(state, ciphertext);

        add_round_key(state, sched + (round * Nb * 4)); 

        for (round = (Nr - 1); round > 0; --round)
        {
          inv_shift_rows(state);
          inv_sub_bytes(state);
          add_round_key(state, sched + (round * Nb * 4));
          inv_mix_columns(state);
        }

        inv_shift_rows(state);
        inv_sub_bytes(state);
        add_round_key(state, sched);
        state_to_plaintext(state, plaintext);
}
